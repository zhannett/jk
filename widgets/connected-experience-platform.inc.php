The Connected Experience Platform is the foundational platform, powering the Connected Experience for service providers.  It leverages the service provider's world-class assets and presents applications like messaging, communications and entertainment in an integrated way to the end user in a secure, intuitive and consistent manner.

The Connected Experience Platform (CEP) includes four major components that form the basis of the product set:

CEP Foundations:

Product foundations provide the data and application foundations necessary for realizing the connected experience.

Federated Identity Module
Social Circle Module
User Process & Rules Module

CEP Applications:

Product applications are application engines that deliver critical user and administrator functions for using and managing connected experiences.

UX Discovery & Rendering
Security & Authentication Engine
User Maintenance Engine
Service Provider Administration Engine

CEP Templates:

Connected Experience Templates are implementations of connected experience lifecycle user process.  Product Experience templates combine user, device and application permutations to create experience templates for typical service provider types (i.e.:  IPTV + IP Voice, Traditional Quadruple Play, MSO Triple Play, etc).  

Connected Experience Templates ship with out of the box set of user interfaces for different devices that may be customized by the service provider to allow for quick implementation and launch.

CEP Customization & Interface Utilities:

Product customization and interface utilities are produced for the purpose of facilitating the tailoring of a solution to the service provider's environment and to enable integration into a service provider's legacy environment.

SDK
API Documentation

The CEP facilitates leveraging the service providers existing assets and accommodating its connected experience limitations.   The suite is intended to cut time to market for initial and subsequent connected experience launches, and is built to evolve with the service provider's connected experience journey – minimizing throwaway development.


