<h3>Introduction</h3>
<p>UXP Systems Connected Experience Platform is an integrated suite of software modules to power the service provider's connected experience for users.  It puts new and must-have capabilities into a service provider's IT infrastructure without creating the need to replace existing legacy assets.</p>
<p>The Connected Experience Platform (CEP) implements the necessary data, process/application and user experience stack required to make the growing need for any-service on any-device a reality.  It unifies user processes, orchestrates them in the service provider's cloud, and not on specific devices – meaning simple, intuitive and "everywhere" user experiences.</p>
<p>The CEP delivers upon transforming the 'service' provider into an experience provider, by layering experience-enabling technology on top of the existing single-service silos in place today in telecom and cable IT environments.</p>
<p>The Connected Experience Platform is the foundation for powering the Connected Experience for service providers.  It leverages the service provider's world-class assets and presents applications like messaging, communications and entertainment in an integrated way to the end user in a secure, intuitive and consistent manner.</p>
<p>The Connected Experience Platform (CEP) includes four major components that form the basis of the product set.</p>

<h3>Functional Components</h3>
<ul class="solutionslist">
	<li>
    	<h4>CEP Foundations:</h4>
        <p>Product foundations provide the data and application foundations necessary for realizing the connected experience.</p>
        <ul>
        	<li>Connected Identity Module</li>
            <li>Social Circle Module</li>
            <li>User Process & Rules Module</li>
        </ul>
    </li>
	<li>
    	<h4>CEP Applications:</h4>
        <p>Product applications are application engines that deliver critical user and administrator functions for using and managing connected experiences.</p>
        <ul>
        	<li>UX Discovery & Rendering</li>
            <li>Security & Authentication Engine</li>
            <li>User Maintenance Engine</li>
            <li>Service Provider Administration Engine</li>
        </ul>
    </li>
    <li>
    	<h4>CEP Templates:</h4>
    	<p>Connected Experience Templates are implementations of connected experience lifecycle user process.  Product Experience templates combine user, device and application permutations to create experience templates for typical service provider types (i.e.:  IPTV + IP Voice, Traditional Quadruple Play, MSO Triple Play, etc).</p>
        <p>Connected Experience Templates ship with out of the box set of user interfaces for different devices that may be customized by the service provider to allow for quick implementation and launch.</p>
    </li>
	<li>
    	<h3>CEP Customization & Interface Utilities:</h3>
    	<p>Product customization and interface utilities are produced for the purpose of facilitating the tailoring of a solution to the service provider's environment and to enable integration into a service provider's legacy environment.</p>
        <ul>
            <li>SDK</li>
            <li>API Documentation</li>
        </ul>
    </li>
</ul>
<p>The CEP facilitates leveraging the service providers existing assets and accommodating its connected experience limitations.   The suite is intended to cut time to market for initial and subsequent connected experience launches, and is built to evolve with the service provider's connected experience journey – minimizing throwaway development.</p>