<h3>Introduction</h3>
<p>Telecom and cable companies can benefit from UXP Systems Connected Identity Management Module, which leverages and enriches existing customer management databases to model the new Connected Identity inside the service provider's infrastructure.  The Connected Identity Framework models each and every end user in the service provider's audience, giving them an individual and personalized experience across devices and applications.</p>
<p>The Connected Identity Management (CIM) Framework, a component of UXP Systems Connected Experience Platform, creates a representation of users inside the service provider's private cloud, while fully and completely leveraging the existing service authentication systems in place in the service provider infrastructure.</p>
<p>The Connected Identity Management Module implements the login once-authenticate-to-many user experience without needing to replace any existing legacy systems.</p>
